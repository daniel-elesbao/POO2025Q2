import java.util.Scanner;

public class NumerosInteiros
{
    public static void main(String[] args)
    {
        long A, B, C;
        Scanner scan = new Scanner(System.in);
        A = scan.nextLong();
        B = scan.nextLong();
        C = scan.nextLong();
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        scan.close();       
    }
}
