import java.util.Scanner;

public class CadeiaDeCaracteres
{
    public static void main(String[] args)
    {
        String A, B, C;
        Scanner scan = new Scanner(System.in);
        A = scan.nextLine();
        B = scan.nextLine();
        C = scan.nextLine();
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);        
        scan.close();
    }
}